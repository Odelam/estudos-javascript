let preco = 19.90;

// 40%
let desconto = 0.4;
let precoComDesconto = preco * (1 - desconto);

// preço do produto menos os 40%
console.log(`Preço com desconto: ${preco * 0.6}. (sem variável)`);

// exemplo de como usar os parênteses para mudar a predecessão matemática
console.log(`Variável preço com desconto: ${precoComDesconto}.`);
